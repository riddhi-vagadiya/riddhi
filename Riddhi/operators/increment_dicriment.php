<?php
  $x = 2;
  echo ++$x, " First increments then prints \n";
  echo $x, "\n";

  $x = 2;
  echo $x++, " First prints then increments \n";
  echo $x, "\n";

  $x = 2;
  echo --$x, " First decrements then prints \n";
  echo $x, "\n";

  $x = 2;
  echo $x--, " First prints then decrements \n";
  echo $x;
?>